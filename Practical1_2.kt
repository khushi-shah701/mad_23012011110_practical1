fun main() {
    var a:Int = 10
    var b:Double = a.toDouble()
    var c:String="10"
    var d=c.toInt()
    var e=c.toInt()
    var f="11.12"
    var g=f.toDouble()
    println("Int value: $a")
    println("Double value: $b")
    println("String value:$c")
    println("Integer value 1:$d")
    println("Integer value 2:$e")
    println("String 2:$f")
    println("Double value(from string):$g")
}